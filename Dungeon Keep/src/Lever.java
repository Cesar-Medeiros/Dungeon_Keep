
public class Lever {

	private int posX;
	private int posY;
	
	public Lever() {
		this.posX = 7;
		this.posY = 8;
	}
	
	public int getPosX() {
		return posX;
	}
	
	public void setPosX(int posX) {
		this.posX = posX;
	}
	
	public int getPosY() {
		return posY;
	}
	
	public void setPosY(int posY) {
		this.posY = posY;
	}
}
