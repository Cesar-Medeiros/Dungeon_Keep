
public class Hero {
	
	private int posX;
	private int posY;
	

	public Hero() {
		this.posX = 1;
		this.posY = 1;
	}

	public int getPosX() {
		return posX;
	}

	public void setPosX(int posX) {
		this.posX = posX;
	}

	public int getPosY() {
		return posY;
	}

	public void setPosY(int posY) {
		this.posY = posY;
	}

}
